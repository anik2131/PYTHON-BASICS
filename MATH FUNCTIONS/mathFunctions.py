from math import *

print(max(4,5)) # return the maximum of two values
print(min(2.5,2.6)) # return the minimum of two values
print(sqrt(36)) # return the square root of a number
print(round(27.4)) # return the round-off value of a number
print(round(29.5)) # if >=0.5 then the corresponding next number
print(floor(27.5)) # returns the number irrespective of the decimal value 
print(ceil(27.4)) # returns the next corresponding number irrespective of the decimal value
print(pow(2,3)) # returns the value of x^y